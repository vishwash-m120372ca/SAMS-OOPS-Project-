    public void DoConnect ()
     {        
        try {
            Class driver_class;
            driver_class = Class.forName("com.mysql.jdbc.Driver");
        } catch (ClassNotFoundException ex){
            System.out.println("Mysql JDBC driver not found");
            System.exit(1);
        }
        
        
        String url = "jdbc:mysql://localhost:3306/sams_db";
        String user = "sams_user";
        String password= "m120361ca";
        Connection connection = null;
        ResultSet result = null;
        Statement st = null;
        
        try {
            connection = DriverManager.getConnection(url, user, password);
            st = connection.createStatement(); 
            st.execute("SELECT * from mess_info WHERE day='"+jComboBox1.getSelectedItem().toString() +"' AND time='"+jComboBox2.getSelectedItem().toString()+"'");
            result = st.getResultSet();
            if(result.next()==false)
            {
                 boolean execute1 = st.execute("INSERT INTO mess_info(day,time,food) VALUES ('"+jComboBox1.getSelectedItem().toString() +"','"+jComboBox2.getSelectedItem().toString() +"','"+jTextField1.getText() +"') ");
                if(execute1==false)
                {
                    jTextField1.setText("");
                    jLabel3.setText("Mess Menu Added :)");
                }
                else
                {
                    jTextField1.setText("");
                    jLabel3.setText("Failed !");
                }
            }
            else
            {
                boolean execute2 = st.execute("UPDATE mess_info SET food='"+jTextField1.getText()+"' WHERE day='"+jComboBox1.getSelectedItem().toString()+"' AND time='"+jComboBox2.getSelectedItem().toString() +"' ");
                if(execute2==false)
                {
                    jTextField1.setText("");
                    jLabel3.setText("Mess Menu Updated :)");
                }
                else
                {
                    jTextField1.setText("");
                    jLabel3.setText("Updation Failed !");
                }
            }
        } catch (SQLException ex){
            jLabel3.setText(" Failed !");
            Logger.getLogger(login.class.getName()).log(Level.SEVERE,null,ex);
        } finally {
            if (result!= null)
            {
                try {
                    result.close();
                } catch (SQLException ex) {
                    Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
                }
                if(st!=null)
                {
                    try {
                        st.close();
                    } catch (SQLException ex) {
                        Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                if(connection != null)
                {
                    try {
                        connection.close();
                    } catch (SQLException ex) {
                        Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }
     }

